export const findAndReplaceLayer = (layers: Array<any>, value) => {
  for (let i = 0; i < layers.length; i++) {
    if (layers[i].nm === 'Bank Mint') {
      layers[i].nm = value;
      break;
    } else if (layers[i].ty === 4 && layers[i].shapes) {
      findAndReplaceLayer(layers[i].shapes, value);
    }
  }
};
